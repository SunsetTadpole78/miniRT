#include "miniRT.h"

unsigned int	fvector3_to_pixel(t_fvector3 c)
{
	return ((int)(fminf(fmaxf(c.x, 0.0f), 1.0f) * 255.0f) << 16
		| (int)(fminf(fmaxf(c.y, 0.0f), 1.0f) * 255.0f) << 8
		| (int)(fminf(fmaxf(c.z, 0.0f), 1.0f) * 255.0f));
}
